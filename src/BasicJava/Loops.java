package BasicJava;

public class Loops {
    public static void main(String[] args)
    {
        //For loop
        for(int i =1; i<10 ; i++){
            System.out.println("I am printing For loop");
        }

        //While Loop
        int n = 5;
        while(n>0){
            System.out.println("I am printing While Loop");
            n--;
        }
    }

}
